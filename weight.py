def pounds_to_stone(pounds):
    stone = pounds / 14
    return stone


def stone_to_pounds(stone):
    pounds = stone * 14
    return pounds


def pounds_to_kilograms(pounds):
    kilograms = pounds * 0.45359237
    return kilograms


def kilograms_to_pounds(kilograms):
    pounds = kilograms * 2.2046
    return pounds


def kilograms_to_stone(kilograms):
    stone = kilograms / 6.35029318
    return stone


def stone_to_kilograms(stone):
    kilograms = stone * 6.35029318
    return kilograms


def ounces_to_grams(ounces):
    grams = ounces * 28.3495231
    return grams


def grams_to_ounces(grams):
    ounces = grams / 28.3495231
    return ounces
